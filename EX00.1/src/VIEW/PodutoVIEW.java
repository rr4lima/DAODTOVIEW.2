    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if (txtNome.getText().trim().isEmpty()
                || txtQtd.getText().trim().isEmpty()
                || txtPreco.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null,
                    "Preencha todos os campos!",
                    "Erro",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        ProdutoDTO p1 = new ProdutoDTO(txtNome.getText(), Integer.parseInt(txtQtd.getText()),
                Double.parseDouble(txtPreco.getText()));
        ProdutoDAO p2 = new ProdutoDAO();
        
        if (p1.getPreco()  <=0) {
            JOptionPane.showMessageDialog(null,
                    "Por favor, insira o valor.",
                    "Erro",
                    JOptionPane.WARNING_MESSAGE);
           
        }
        
        if (p1.getQtd() <=0 ){
             JOptionPane.showMessageDialog(null,
                    "A quantidade precisa ser maior que zero.",
                    "Erro",
                    JOptionPane.WARNING_MESSAGE);
            return;
        } 

        p2.adicionarProduto(p1);
        p2.listarProdutos();

    }//GEN-LAST:event_btnCadastrarActionPerformed

