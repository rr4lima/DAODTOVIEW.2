    private void btnAdcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdcActionPerformed

        
        RestauranteDTO r1 = new RestauranteDTO(Integer.parseInt(txtnPedido.getText()), txtCliente.getText(), txtItens.getText(),
                Double.parseDouble(txtTotal.getText()));
        
                if (r1.getCliente().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null,
                    "Preencha todos os campos..",
                    "Erro",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }
        dao.adcPedido(r1);
        System.out.println("Pedido adicionado!");
    }//GEN-LAST:event_btnAdcActionPerformed

